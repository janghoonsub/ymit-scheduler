package wb.ml.schedule.week;

public class WeekInfo {
	String weekAndDate;
	String mSchedule;
	
	public String getWeekAndDate() {
		return weekAndDate;
	}
	public void setWeekAndDate(String weekAndDate) {
		this.weekAndDate = weekAndDate;
	}
	public String getmSchedule() {
		return mSchedule;
	}
	public void setmSchedule(String mSchedule) {
		this.mSchedule = mSchedule;
	}
	
	
}
